    public static function countiesByState($stateCode){
        $coutiesFull = self::counties();
        $returnArray = array();
        foreach ($coutiesFull as $key=>$value){
            if(substr($key, 0, 2) == $stateCode){
                $returnArray[$key] = $value;
            }
        }
        return $returnArray;
    }
